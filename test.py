class StickMan:
    def __init__(self):
        self.head = 'O'
        self.hand_left = '/'
        self.body = '|'
        self.hand_right = "\\"
        self.foot_left = '/'
        self.foot_right = '\\'
    def head(self):
        print(self.head)
    def hand_left(self):
        print(self.hand_left)
    def body(self):
        print(self.body)    
    def hand_right(self):
        print(self.hand_right)
    def foot_left(self):
        print(self.foot_left)
    def foot_right(self):
        print(self.foot_right)                        

